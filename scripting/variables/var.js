
// var are function scoped or globle scoped but not block scoped -->  It can be redefine and reassign 

var x = 8;
console.log(x);

//  Re asignment using var 
 x = "Aayushi";
 console.log(x);


// Re definition using var
 var x =  "Aastha";
 console.log(x);


//  let is block scoped variable --->> It can be Re assign but not Redefine.

let y = 15;
console.log(y);

// Re assign-->

y = "Sarvesh";
